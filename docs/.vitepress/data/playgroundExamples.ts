function normalizeDocsRoute(route: string) {
  return route.replace(/\/$/, '')
}

const playgroundComponentByDocsRoute = new Map<string, PlaygroundComponent>()

playgroundComponents.forEach((component) => {
  const docsRoute = normalizeDocsRoute(playgroundComponentDocs[component])

  if (!playgroundComponentByDocsRoute.has(docsRoute)) {
    playgroundComponentByDocsRoute.set(docsRoute, component)
  }
})

export function getPlaygroundComponentForDocsRoute(route: string) {
  return playgroundComponentByDocsRoute.get(normalizeDocsRoute(route)) ?? ''
}

