const familyTitleById = Object.fromEntries(
  componentFamilies.map((family) => [family.id, family.title])
)

export const playgroundComponentOptions = playgroundComponents.map((component) => {
  const docs = playgroundComponentDocs[component]
  const registryName = playgroundComponentRegistryName[component]
  const registryMeta = registryName
    ? components.find((item) => item.name === registryName)
    : components.find((item) => item.docs === docs)

  if (!registryMeta) {
    throw new Error(`Missing component registry metadata for playground component "${component}" at ${docs}.`)
  }

  return {
    name: registryMeta.name,
    label: registryMeta.title,
    value: component,
    family: familyTitleById[registryMeta.family] ?? registryMeta.family,
    description: registryMeta.description,
    packageName: registryMeta.packageName,
    docs
  }
})

